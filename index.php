                    <p class="text-faded" text-align="left">Em meados de 2012, a Região Nordeste foi atingida por uma situação de emergência decorrente da seca, onde a falta de chuva secou rios; prejudicou as lavouras, pois na terra esturricada não dava para plantar; deixando os animais famintos, sem ter o que comer; fez disparar o preço de alimentos; em resumo, um cenário de total desolação.

Quando se tornou professor, o Fundador Guido Cavalcanti sentiu que, a melhor maneira de ensinar era colocar o aluno diante do caso prático. Diante disso, o melhor caso prático possível seria o problema social, fornecendo aprendizado e uma visão ampla, desperta e profundamente maior, através do conhecimento ministrado dentro de sala de aula e do envolvimento do aluno nas ações em si, fazendo um ciclo perfeito de sustentabilidade entre: aprendizado, solidariedade e auxílio.

Com objetivo de minimizar – no que for possível – o sofrimento dos sertanejos atingidos pela longa estiagem, nasceu no meio universitário a ONG Salve Sertão, entidade de iniciativa privada, sem fins lucrativos, sem filiação partidária ou instintuições religiosas, tendo como Fundador o Professor Guido Cavalcanti e, sendo administrada por voluntários.</p>
